// -------------------------------------------------------------------------- //

#include <nusys.h>

#include "app.hpp"
#include "heap.hpp"
// -------------------------------------------------------------------------- //

extern "C" {

void mainproc(void)
{
  bootApp();
}

}

// -------------------------------------------------------------------------- //

void bootApp()
{
  CApp app = CApp();

  // 0.5 MB heap
  auto gameHeap = TBlockHeap(
  reinterpret_cast<void *>(kHeapLocation), kHeapSize);

  // set game heap as current heap
  THeap::setCurrentHeap(&gameHeap);

  app.init();
  app.run();
}

